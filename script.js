//navigates to a random project page that is Not the one the user is currently on
function imFeelingLucky() {
  //use current path name to avoid opening the proj that user is already in
  //function will be a loop until a non-current project is chosen
  const currentUrl = window.location.href;
  let urlPieces = currentUrl.split("#");
  let samePage = true;
  let currProjUser = "";
  let newProjUser = "";
  let index = -1;
  //IF length==2 (>1) of urlPieces, then we are currently on a page of sorts and we should check that whatever is generated is Not == current proj username
  //let's loop until we get a good one
  while (samePage) {
    if (urlPieces.length > 1) {
      currProjUser = urlPieces[1];
    }
    index = Math.floor(Math.random() * projects.length);
    newProjUser = projects[index].username;
    if (!(currProjUser === newProjUser)) {
      samePage = false;
    }
  }
  //once we have a vetted newProjUser, open sesame
  let projectlinkname = newProjUser + "link";
  let projectlink = document.getElementById(projectlinkname);
  projectlink.click();
}

