//helper to rearrange the photo order randomly
function randomizeOrder() {
  let oldphotos = photos;
  let randomorderphotos = [];
  let randonum = 0;
  let chosenphoto = 0;

  while (oldphotos.length > 0) {
    let numphotos = oldphotos.length; // picking randomly from what's left
    randonum = Math.floor(Math.random() * numphotos);
    randomorderphotos.push(oldphotos[randonum]);

    // removing from old photos array
    if (randonum > -1) {
      // only splice array when item is found
      oldphotos.splice(randonum, 1); // 2nd parameter means remove one item only
    }
  }

  return randomorderphotos;
}

